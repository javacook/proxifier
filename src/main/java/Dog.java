public class Dog {

    private String race;
    private int weight;

    public void bark() {
        System.out.println("Woof!");
    }

    public String getRace() {
        return race;
    }

    public void setRace(String race) {
        this.race = race;
    }


    public int getWeight() {
        return weight;
    }


    public void setWeight(int weight) {
        this.weight = weight;
    }
}